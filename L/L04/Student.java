public class Student {
    String name;
    String stdid;
    String gender;
    String address;
    String phoneNumber;
    boolean isReturnStudent;

    public Student(String name, String stdid, String gender, String address, String phoneNumber, boolean isReturnStudent) {
        this.name = name;
        this.stdid = stdid;
        this.gender = gender;
        this.address = address;
        this.phoneNumber = phoneNumber;
        this.isReturnStudent = isReturnStudent;
    }
}
